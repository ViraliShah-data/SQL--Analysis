-- Q-1.List all Students from India
select name,country 
from students
where country = "India";

-- Q-2.Show all courses under data category
select course_name,category
from courses
where category = "Data";

-- Q-3.Get student name with courses name
select s.name,c.course_name
from enrollments e
join courses c
on e.course_id = c.course_id
join students s
on e.student_id = s.student_id;

-- Q-4.Find all students who completed the course
select s.name,e.completion_status,c.course_name
from students s
join enrollments e
on s.student_id = e.student_id
join courses c
on c.course_id = e.course_id
where completion_status = "completed";

-- Q-5.Show average rating per course
select avg(e.rating) as Avg_rating,c.course_name
from enrollments e
join courses c
on c.course_id = e.course_id
where e.rating is not null
group by c.course_name;

-- Q-6.Top rated courses
select avg(e.rating) as Avg_rating,c.course_name
from enrollments e
join courses c
on c.course_id = e.course_id
where e.rating is not null
group by c.course_name
order by Avg_rating desc;

-- Q-7. Most active Student
select count(e.enrollment_id) as Total_Courses, s.name 
from enrollments e
join students s
on s.student_id = e.student_id
group by s.name
order by Total_Courses desc
limit 1;

-- Q-8.Revenue generated by category
select sum(c.price) as revenue, c.category
from courses c
join enrollments e
on c.course_id = e. course_id
where e.completion_status = "Completed"
group by category;

-- Q-9. Ranking
select *,
rank() over (order by avg_rating desc) as Ranking 
from ( 
		select avg(e.rating) as avg_rating, c.course_name
        from enrollments e
        join courses c
        on c.course_id = e.course_id
        where e.rating is not null
        group by c.course_name) t;
        
-- Q-10. Top 2 Courses per category
select * 
from (
		select c.category,c.course_name, avg(e.rating) as Avg_rating,
        rank() over (partition by c.category order by avg(e.rating) desc) as Ranks
        from enrollments e
        join courses c
        on c.course_id = e.course_id
        where e.rating is not null
        group by c.category,c.course_name) Top
where ranks <=2;

-- Q-11. Students Who Gave Above Average Rating
select s.name,e.rating
from enrollments e
join students s
on s.student_id = e.student_id
where e.rating > (
	select avg(rating) as Avg
    from enrollments
    where rating is not null);
    
-- Q-12.Course Completion Rate 
select c.course_name,
	   count(case when e.completion_status = "Completed" then 1 end)*100 / count(*)  as completion_rate
from enrollments e
join courses c
on c.course_id = e.course_id
group by c.course_name;

-- Q-13. Most Valuable Student
select s.name,sum(c.price) as amount
from enrollments e
join students s on s.student_id=e.student_id
join courses c on c.course_id=e.course_id
where e.completion_status = "Completed"
group by s.name
order by amount desc
limit 1;

-- Q-14.Courses with No Enrollments
select c.course_name,e.enrollment_id
from courses c
left join enrollments e
on e.course_id=c.course_id
where e.course_id is null;

-- Q-15.The highest spending student in each country
select * 
from(
	select  s.name, sum(c.price) as Total_Spent , s.country, 
			rank() over (partition by s.country order by sum(c.price) desc) as Ranking
	from enrollments e
	join students s on s.student_id=e.student_id
	join courses c on c.course_id=e.course_id
	where e.completion_status = "Completed"
	group by s.country,s.name ) top
where ranking = 1;
    



